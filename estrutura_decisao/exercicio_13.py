dia_semana = int(input(" ---- 1 - Domingo \n"
                       " ---- 2 - Segunda-Feira \n"
                       " ---- 3 - Terça-Feira \n"
                       " ---- 4 - Quarta-Feira \n"
                       " ---- 5 - Quinta-Feira \n"
                       " ---- 6 - Sexta-Feira \n"
                       " ---- 7 - Sábado \n"
                       "Digite o número correspondente ao dia: "));

if(dia_semana == 1):
    print("Hoje é Domingo.");
elif(dia_semana == 2):
    print("Hoje é Segunda-Feira");
elif(dia_semana == 3):
    print("Hoje é Terça-Feira");
elif(dia_semana == 4):
    print("Hoje é Quarta-Feira");
elif(dia_semana == 5):
    print("Hoje é Quinta-Feira");
elif(dia_semana == 2):
    print("Hoje é Sexta-Feira");
elif(dia_semana == 2):
    print("Hoje é Sábado");
else:
    print("Valor inválido!");